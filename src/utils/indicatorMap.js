export function indicatorLabel(code) {
  const item = INDICATORS_WITH_DESC.find(i => i.code === code);
  if (!item) return code;
  const short = item.desc.length > 50 ? item.desc.substring(0, 50) + "…" : item.desc;
  return `${code} — ${short}`;
}

